use std::fs;

use crate::{report::QuerySpec, util::temp_dir};

use super::*;

#[test]
fn fallback_quality_query_reports_generated_and_vendor_paths() {
    let root = temp_dir("fallback-quality-query").unwrap();
    fs::create_dir_all(root.join("src")).unwrap();
    fs::create_dir_all(root.join("vendor")).unwrap();
    fs::write(
        root.join("Cargo.toml"),
        "[package]\nname = \"fallback_quality_query\"\nversion = \"0.1.0\"\nedition = \"2024\"\n",
    )
    .unwrap();
    fs::write(root.join("src/lib.rs"), "pub fn local() {}\n").unwrap();
    fs::write(
        root.join("src/generated.rs"),
        "// @generated by benchmark test\npub fn generated() {}\n",
    )
    .unwrap();
    fs::write(root.join("vendor/lib.rs"), "pub fn vendored() {}\n").unwrap();

    let build = build_graph(&root).unwrap();
    let fallback = fallback_quality_report(
        &build.coverage,
        build.unsupported_files,
        build.unsupported_file_samples,
        &build.graph,
    );
    let query = QuerySpec {
        id: "fallback-quality".to_string(),
        kind: "fallback_quality".to_string(),
        text: String::new(),
        symbol_kind: None,
        owner_kind: None,
        attribute: None,
        from: None,
        to: None,
        expected_contains: vec!["generated".to_string(), "vendor".to_string()],
        documented_misses: Vec::new(),
        baseline: None,
    };

    let report = run_query(&root, &build.graph, &query, &fallback).unwrap();

    assert!(report.missing.is_empty(), "{:?}", report);
    assert!(report.actual.contains(&"generated".to_string()));
    assert!(report.actual.contains(&"vendor".to_string()));
}
